package com.ktck124.lop124LTDD04.nhom18.Domain;

public class ThanhVien {
    private String hoten, masv;

    public ThanhVien(String hoten, String masv) {
        this.hoten = hoten;
        this.masv = masv;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    public String getMasv() {
        return masv;
    }

    public void setMasv(String masv) {
        this.masv = masv;
    }
}
